import unittest

import main
import srcs.main

class MyTestCase(unittest.TestCase):
    def test_sort_positions(self):
        positions = [
            [100, 101, 50, 50],
            [200, 200, 50, 50],
            [300, 301, 50, 50],
            [400, 100, 50, 50],
            [500, 201, 50, 50],
            [600, 300, 50, 50],
            [700, 101, 50, 50],
            [100, 200, 50, 50],
            [200, 301, 50, 50],
            [300, 102, 50, 50],
            [400, 200, 50, 50],
            [500, 302, 50, 50],
            [600, 105, 50, 50],
            [700, 203, 50, 50],
            [100, 303, 50, 50],
            [200, 102, 50, 50],
            [300, 200, 50, 50],
            [400, 301, 50, 50],
            [500, 102, 50, 50],
            [600, 200, 50, 50],
            [700, 303, 50, 50],
        ]

        expected = [
            [100, 303, 50, 50],
            [200, 301, 50, 50],
            [300, 301, 50, 50],
            [400, 301, 50, 50],
            [500, 302, 50, 50],
            [600, 300, 50, 50],
            [700, 303, 50, 50],
            [100, 200, 50, 50],
            [200, 200, 50, 50],
            [300, 200, 50, 50],
            [400, 200, 50, 50],
            [500, 201, 50, 50],
            [600, 200, 50, 50],
            [700, 203, 50, 50],
            [100, 101, 50, 50],
            [200, 102, 50, 50],
            [300, 102, 50, 50],
            [400, 100, 50, 50],
            [500, 102, 50, 50],
            [600, 105, 50, 50],
            [700, 101, 50, 50],
        ]

        result = main.sorted_positions(positions)
        self.assertEqual(expected, result)


